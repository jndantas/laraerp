    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $sector = Sector::findOrFail($id);
        $sector->enterprises()->detach($sector->id);
        $sector->delete();
        return ['message' => 'Deletado'];
    }
